package TiaraRamadhani;

public class Days25 {
    public static void main(String[] args) {
        System.out.println("Perulangan Increment:");
        for (int i = 1; i <= 5; i++) {
            System.out.println("i = " + i);
        }
        System.out.println("Perulangan Decrement:");
        for (int j = 5; j >= 1; j--) {
            System.out.println("j = " + j);
        }
    }
}
