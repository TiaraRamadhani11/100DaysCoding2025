package TiaraRamadhani;

public class Days6 {
    public static void main(String[] args) {
        double Angka_double = 383.455; //tipe data double di gunakan untuk angka desimal
        float Angka_float = 473.46f; //tipe data float sama seperti double tapi kalau float di tambahkan huruf f di akhir angka

        System.out.println("double : " + Angka_double);
        System.out.println("float : " + Angka_float);
    }
}
