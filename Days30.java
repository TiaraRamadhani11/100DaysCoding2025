package TiaraRamadhani;

public class Days30 {
    public static void main(String[] args) {
        //operator perbandingan <= (lebih kecil sama dengan) dan >= (lebih besar sama dengan)

        int angka1 = 11;
        int angka2 = 10;
        int angka3 = 11;

        System.out.println(angka1 <= angka2);//false
        System.out.println(angka1 >= angka2);//true
        System.out.println(angka1 <= angka3);//true

    }
}
